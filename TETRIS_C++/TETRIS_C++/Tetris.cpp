int blockX = 0, blockY = 0, blockLotation = 0, blockType = 0;
enum { MOVEBLOCK = 0, SHADOWBLOCK, FIXBLOCK, WALL, DEADLINE, EMPTY };

void NewBlockMaker()
{
	blockType = rand() % 7;
	blockX = 4;
	blockY = 0;
	blockLotation = 0;

	for (int y = 0; y < 4; y++) {
		for (int x = 0; x < 4; x++) {
			if (blocks[blockType][blockLotation][y][x] == 1) {
				Board[blockY + y][blockX + x] = MOVEBLOCK;
			}
		}
	}
}